/**
 * Get color value for a specific theme and mode
 */
export function getColor(
  theme: ThemeName,
  colorKey: keyof typeof colors.default,
  mode: ColorMode = 'dark'
): string {
  const themeColors = colors[theme];
  const key = `${colorKey.replace(/-light$|-dark$/, '')}-${mode}` as keyof typeof themeColors;
  return themeColors[key] || colors.default[key];
}

/**
 * Get font family for a specific theme
 */
export function getFont(theme: ThemeName, type: 'sans' | 'serif' | 'mono' = 'sans'): string {
  return fonts[theme]?.[type] || fonts.default[type];
}

/**
 * Get radius value for a specific theme
 */
export function getRadius(theme: ThemeName, size: keyof typeof radius.default): number {
  return radius[theme]?.[size] || radius.default[size];
}

/**
 * Get shadow value for a specific theme
 */
export function getShadow(theme: ThemeName, size: keyof typeof shadow.default): string {
  return shadow[theme]?.[size] || shadow.default[size];
}

/**
 * Generate CSS custom properties for a theme
 */
export function generateCSSVariables(theme: ThemeName, mode: ColorMode = 'dark'): Record<string, string> {
  const themeColors = colors[theme];
  const variables: Record<string, string> = {};

  // Colors
  Object.keys(themeColors).forEach((key) => {
    if (key.endsWith(`-${mode}`)) {
      const varName = `--${key.replace(`-${mode}`, '')}`;
      variables[varName] = themeColors[key as keyof typeof themeColors];
    }
  });

  // Typography
  const themeFont = fonts[theme];
  variables['--font-sans'] = themeFont.sans;
  variables['--font-serif'] = themeFont.serif;
  variables['--font-mono'] = themeFont.mono;

  // Radius
  const themeRadius = radius[theme];
  Object.entries(themeRadius).forEach(([size, value]) => {
    variables[`--radius-${size}`] = `${value}px`;
  });

  // Shadows
  const themeShadow = shadow[theme];
  Object.entries(themeShadow).forEach(([size, value]) => {
    variables[`--shadow-${size}`] = value;
  });

  return variables;
}

